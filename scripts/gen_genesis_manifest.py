def _load_registry() -> list[dict]:
    if not REGISTRY.exists():
        print(
            f"error: {REGISTRY} missing — run gen_mcp_fleet_registry.py",
            file=sys.stderr,
        )
        raise SystemExit(2)
    data = yaml.safe_load(REGISTRY.read_text(encoding="utf-8")) or {}
    return data.get("services", [])


