"""Generate ``genesis.yaml`` — the single index an agent loops to self-deploy.

``genesis.yaml`` is the machine-readable contract behind the one-link deploy: an
agent (or ``scripts/install.sh``) reads it to pick a profile, run the host
preflight, install, then wire the MCP fleet and any chosen UI components. It
**composes** sources that already exist rather than duplicating them:

* the MCP fleet + per-service profiles come from ``deploy/mcp-fleet.registry.yml``
  (itself generated by ``gen_mcp_fleet_registry.py``);
* the deployment profiles mirror ``agent_utilities.deployment.config_generator.PROFILES``
  and the ``docs/recipes/*`` runbooks;
* the preflight describes ``agent-utilities-doctor --preflight``;
* the IDE targets mirror the ``install-skills`` / ``mcp-installer`` tool sets.

Because it is generated, drift is caught by ``scripts/check_genesis_manifest.py``
(a CI gate) — never hand-edit ``genesis.yaml``.

Usage:
    python scripts/gen_genesis_manifest.py --out genesis.yaml
"""

